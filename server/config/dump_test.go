func TestRenderNonDefaultConfigDump_RedactsSensitiveValuesByDefault(t *testing.T) {
	SetConfigDumpPrintSensitiveValues(false)

	settings := map[string]any{
		"auth": map[string]any{
			"request": map[string]any{
				"headers": map[string]any{
					"password":         "super-secret",
					"password_encoded": "c3VwZXItc2VjcmV0",
				},
			},
		},
		"identity": map[string]any{
			"saml": map[string]any{
				"key": "private-key",
			},
		},
	}

	output, err := RenderNonDefaultConfigDump(settings)
	if err != nil {
		t.Fatalf("RenderNonDefaultConfigDump() error = %v", err)
	}

	expectedLines := []string{
		`auth.request.headers.password = "***REDACTED***"`,
		`auth.request.headers.password_encoded = "***REDACTED***"`,
		`identity.saml.key = "***REDACTED***"`,
	}

	for _, expected := range expectedLines {
		if !strings.Contains(output, expected) {
			t.Fatalf("RenderNonDefaultConfigDump() missing redacted line %q", expected)
		}
	}

	if strings.Contains(output, "super-secret") || strings.Contains(output, "private-key") {
		t.Fatalf("RenderNonDefaultConfigDump() unexpectedly exposed sensitive output: %q", output)
	}
}

