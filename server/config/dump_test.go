func TestParseDumpFormat(t *testing.T) {
	testCases := []struct {
		name    string
		input   string
		want    DumpFormat
		wantErr bool
	}{
		{name: "default empty", input: "", want: DumpFormatCanonical},
		{name: "canonical", input: "canonical", want: DumpFormatCanonical},
		{name: "yaml uppercase", input: "YAML", want: DumpFormatYAML},
		{name: "json padded", input: " json ", want: DumpFormatJSON},
		{name: "toml", input: "toml", want: DumpFormatTOML},
		{name: "invalid", input: "ini", wantErr: true},
	}

	for _, testCase := range testCases {
		t.Run(testCase.name, func(t *testing.T) {
			got, err := ParseDumpFormat(testCase.input)
			if testCase.wantErr {
				if err == nil {
					t.Fatal("ParseDumpFormat() error = nil, want error")
				}

				return
			}

			if err != nil {
				t.Fatalf("ParseDumpFormat() error = %v", err)
			}

			if got != testCase.want {
				t.Fatalf("ParseDumpFormat() = %q, want %q", got, testCase.want)
			}
		})
	}
}

