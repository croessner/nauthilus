func TestRenderDefaultConfigDump_IncludesKnownDefaults(t *testing.T) {
	SetConfigDumpVersion("3.0.0-test")
	t.Cleanup(func() {
		SetConfigDumpVersion("")
	})

	output, err := RenderDefaultConfigDump()
	if err != nil {
		t.Fatalf("RenderDefaultConfigDump() error = %v", err)
	}

	expectedHeader := "# Nauthilus canonical default configuration\n# Generated by `nauthilus -d`.\n# Version: 3.0.0-test\n"
	if !strings.HasPrefix(output, expectedHeader) {
		t.Fatalf("RenderDefaultConfigDump() missing header %q in output %q", expectedHeader, output)
	}

	expectedLines := []string{
		`runtime.http.middlewares.logging = true`,
		`runtime.listen.tls.min_tls_version = "TLS1.2"`,
		`runtime.http.timeouts.lua_script = "30s"`,
		`identity.oidc.tokens.revoke_refresh_token = true`,
	}

	for _, expected := range expectedLines {
		if !strings.Contains(output, expected) {
			t.Fatalf("RenderDefaultConfigDump() missing line %q", expected)
		}
	}
}

func TestRenderNonDefaultConfigDump_SuppressesDefaultsAndKeepsChanges(t *testing.T) {
	SetConfigDumpVersion("3.0.0-test")
	t.Cleanup(func() {
		SetConfigDumpVersion("")
	})

	settings := map[string]any{
		"developer_mode": false,
		"runtime": map[string]any{
			"http": map[string]any{
				"middlewares": map[string]any{
					"logging": true,
					"limit":   false,
				},
			},
			"listen": map[string]any{
				"tls": map[string]any{
					"min_tls_version": "TLS1.3",
				},
			},
		},
	}

	output, err := RenderNonDefaultConfigDump(settings)
	if err != nil {
		t.Fatalf("RenderNonDefaultConfigDump() error = %v", err)
	}

	expectedHeader := "# Nauthilus canonical non-default configuration\n# Generated by `nauthilus -n`.\n# Version: 3.0.0-test\n"
	if !strings.HasPrefix(output, expectedHeader) {
		t.Fatalf("RenderNonDefaultConfigDump() missing header %q in output %q", expectedHeader, output)
	}

	if strings.Contains(output, `runtime.http.middlewares.logging = true`) {
		t.Fatalf("RenderNonDefaultConfigDump() unexpectedly kept default-valued line: %q", output)
	}

	if strings.Contains(output, `developer_mode = false`) {
		t.Fatalf("RenderNonDefaultConfigDump() unexpectedly kept default developer_mode: %q", output)
	}

	expectedLines := []string{
		`runtime.http.middlewares.limit = false`,
		`runtime.listen.tls.min_tls_version = "TLS1.3"`,
	}

	for _, expected := range expectedLines {
		if !strings.Contains(output, expected) {
			t.Fatalf("RenderNonDefaultConfigDump() missing line %q", expected)
		}
	}
}

