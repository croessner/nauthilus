func TestRenderDefaultConfigDumpWithStructuredFormats_ProducesLoadableDocuments(t *testing.T) {
	SetConfigDumpVersion("3.0.0-test")
	t.Cleanup(func() {
		SetConfigDumpVersion("")
	})

	for _, format := range []DumpFormat{
		DumpFormatYAML,
		DumpFormatJSON,
		DumpFormatTOML,
	} {
		t.Run(string(format), func(t *testing.T) {
			output, err := RenderDefaultConfigDumpWithFormat(format)
			if err != nil {
				t.Fatalf("RenderDefaultConfigDumpWithFormat() error = %v", err)
			}

			parsed := parseStructuredConfigDumpOutput(t, format, output)
			assertStructuredDumpValue(t, parsed, []string{"runtime", "http", "middlewares", "logging"}, true)
			assertStructuredDumpValue(t, parsed, []string{"runtime", "listen", "tls", "min_tls_version"}, "TLS1.2")
		})
	}
}

func runStructuredRoundTripCase(t *testing.T, format DumpFormat, settings map[string]any) {
	t.Helper()

	t.Run(string(format), func(t *testing.T) {
		output, err := RenderNonDefaultConfigDumpWithFormat(settings, format)
		if err != nil {
			t.Fatalf("RenderNonDefaultConfigDumpWithFormat() error = %v", err)
		}

		parsed := parseStructuredConfigDumpOutput(t, format, output)
		assertStructuredDumpRoundTripValues(t, parsed)
		assertStructuredDumpVersionHeader(t, format, output)
	})
}

func assertStructuredDumpRoundTripValues(t *testing.T, parsed map[string]any) {
	t.Helper()

	assertStructuredDumpValue(t, parsed, []string{"runtime", "instance_name"}, "mini")
	assertStructuredDumpValue(t, parsed, []string{"runtime", "http", "middlewares", "limit"}, false)
	assertStructuredDumpValue(t, parsed, []string{"storage", "redis", "primary", "address"}, "localhost:6379")
	assertStructuredDumpValue(t, parsed, []string{"storage", "redis", "password_nonce"}, "nonce-secret-1234")
	assertStructuredDumpValue(t, parsed, []string{"identity", "session", "remember_me_ttl"}, "24h")
}

func assertStructuredDumpVersionHeader(t *testing.T, format DumpFormat, output string) {
	t.Helper()

	switch format {
	case DumpFormatYAML, DumpFormatTOML:
		if !strings.Contains(output, "# Version: 3.0.0-test") {
			t.Fatalf("structured output missing version header: %q", output)
		}
	case DumpFormatJSON:
		if strings.Contains(output, "# Version: 3.0.0-test") {
			t.Fatalf("json output must not contain comment header: %q", output)
		}
	}
}

func parseStructuredConfigDumpOutput(t *testing.T, format DumpFormat, output string) map[string]any {
	t.Helper()

	document := stripStructuredConfigDumpHeader(output)
	parsed := make(map[string]any)

	switch format {
	case DumpFormatYAML:
		if err := yaml.Unmarshal([]byte(document), &parsed); err != nil {
			t.Fatalf("yaml unmarshal failed: %v\noutput=%s", err, output)
		}
	case DumpFormatJSON:
		if err := json.Unmarshal([]byte(document), &parsed); err != nil {
			t.Fatalf("json unmarshal failed: %v\noutput=%s", err, output)
		}
	case DumpFormatTOML:
		if err := toml.Unmarshal([]byte(document), &parsed); err != nil {
			t.Fatalf("toml unmarshal failed: %v\noutput=%s", err, output)
		}
	default:
		t.Fatalf("unsupported format %q", format)
	}

	return parsed
}

func stripStructuredConfigDumpHeader(output string) string {
	lines := strings.Split(output, "\n")
	start := 0

	for start < len(lines) {
		line := strings.TrimSpace(lines[start])
		if line == "" || strings.HasPrefix(line, "#") {
			start++

			continue
		}

		break
	}

	return strings.Join(lines[start:], "\n")
}

func assertStructuredDumpValue(t *testing.T, root map[string]any, path []string, expected any) {
	t.Helper()

	current := any(root)
	for _, segment := range path {
		mapped, ok := current.(map[string]any)
		if !ok {
			t.Fatalf("path %v does not resolve to a map at segment %q (got %T)", path, segment, current)
		}

		next, ok := mapped[segment]
		if !ok {
			t.Fatalf("path %v missing segment %q", path, segment)
		}

		current = next
	}

	switch want := expected.(type) {
	case bool:
		got, ok := current.(bool)
		if !ok || got != want {
			t.Fatalf("path %v = %#v (%T), want %v", path, current, current, want)
		}
	case string:
		got, ok := current.(string)
		if !ok || got != want {
			t.Fatalf("path %v = %#v (%T), want %q", path, current, current, want)
		}
	default:
		if current != expected {
			t.Fatalf("path %v = %#v, want %#v", path, current, expected)
		}
	}
}
